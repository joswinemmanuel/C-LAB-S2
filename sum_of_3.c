#include <stdio.h>
// Sum of three
int main(){
    int a,b,c;
    printf("Enter the first number : ");
    scanf("%d",&a);
    printf("Enter the second number : ");
    scanf("%d",&b);
    printf("Enter the third number : ");
    scanf("%d",&c);
    printf("%d + %d + %d = %d",a,b,c,a+b+c);
    return 0;
}
